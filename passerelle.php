Lignes brisées<p><br></p> 

Installation de Loïc Hervé, 1999<p><br></p>



  		Lorsque les réfugiés Huttus ou Tutsis, pas toujours très innocents, des massacres du Ruanda se sont regroupés dans des camps d&rsquo;accueil, ils ont été contraints de laisser leurs armes blanches. On a vu alors des monceaux de longues machettes abandonnées. Ces machettes sont les mêmes que celles des esclaves coupeurs de canne à sucre des Antilles. Outils de vie / armes sanglantes.<br><br>


 		L&rsquo;installation de Loïc Hervé réunit d&rsquo;autres outils aussi ambivalents. Par dizaines, haches, pioches de creuseurs de mine ou de fossé, fourches de ramasseurs de foin ou de gros cailloux. Pioches des briseurs du mur de Berlin, fourches des paysans de 1789. Et l&rsquo;outil qui prolonge la main et médiatise le corps est ce qui libère l&rsquo;homme.<br><br>


 		Mais Loïc Hervé plante pioches et fourches dans des postures inattendues et ambivalentes. Joyeuses elles dansent sur le sol et jubilent en grimpant sur le mur. Furieuses elles agressent de leurs pointes sol et murs. Par dizaines, meurtres, coups, violence. Violence en cours. Les cousines des machettes du Ruanda officient encore ici. Non pas amoncellement, mais chorégraphie bizarre, éparpillée, énergique.<br><br>


  		Or les manches des haches, des pioches et des fourches de Loïc Hervé sont brisés. L&rsquo;outil est devenu inopérant. La force de travail est vacante et inactive, le chômage partout. Mais aussi : les manches sont, non pas vraiment fracturés, mais articulés, avec de simples chevilles de bois. Curieux membres, bras, jambes. Nous sommes près de l&rsquo;atelier d&rsquo;un nouveau Gepetto ; ses Pinocchio en bois articulés s&rsquo;agitent, s&rsquo;ébrouent, s&rsquo;éparpillent, sortent dans la rue, courent, grimpent au mur. Par dizaines, enfants ligneux et ferreux d&rsquo;un rêve dur, les outils&ndash;personnages sont maintenant devant nous, attendant que nous leur serrions la main. Ils sont vierges, encore non utilisés. Mais. Mais ils cognent, mais ils piquent. Mais ils font mal. Ambivalence de tout acte humain.<br><br>


 		Mais ils crient. Voir l&rsquo;installation de Loïc Hervé, c&rsquo;est l&rsquo;entendre puis l &lsquo;écouter. Cette danse follement  démultipliée, jetée sur la chair du sol et sur la chair des murs, c&rsquo;est le bruit des chocs et des coups par dizaines, c&rsquo;est un martèlement de percussion, c&rsquo;est un staccato de cris et de chocs, tous ensemble. C&rsquo;est le bruit insistant, têtu, innombrable, du silence qui rôde et s&rsquo;impatiente en nous, dont nous ne savons s&rsquo;il va prendre l&rsquo;orientation solaire d&rsquo;une musique de vie, ou l&rsquo;orientation lunaire d&rsquo;une musique de mort, ou s&rsquo;il n&rsquo;est, en fait, les deux à la fois.<br><br>  


Yves Bergeret<p><br><br></p>
  
